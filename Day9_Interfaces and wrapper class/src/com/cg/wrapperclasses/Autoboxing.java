package com.cg.wrapperclasses;
//Autoboxing:Converting primitive type to its respective wrapper class objects
public class Autoboxing {

	public static void main(String[] args) {
		float x=29.9f; //primitive type
		Float a=x; //converting primitive to wrapper class
		Float f=new Float(12);
		System.out.println(x+" "+a+" "+f);
		
	}

}
